export interface ArchetypeScore {
  key: ArchetypeKey;
  name: string;
  score: number;
  percent: number; // округлённый до 1 знака
}

export interface ArchetypeResult {
  scores: ArchetypeScore[]; // все 12, отсортированы по убыванию (tie-break по ARCHETYPE_ORDER)
  top: ArchetypeScore[]; // топ-3
  tiedTop: boolean; // true, если у нескольких архетипов в топе одинаковый максимум
  tiedCount: number; // сколько архетипов делят максимум
}

// Подсчёт результата. answers: { q1: 'A', ... }
export function calculateArchetypeResult(
  answers: Record<string, OptionLetter>,
): ArchetypeResult {
  const counts: Record<ArchetypeKey, number> = {
    innocent: 0,
    explorer: 0,
    sage: 0,
    hero: 0,
    rebel: 0,
    magician: 0,
    everyman: 0,
    lover: 0,
    jester: 0,
    caregiver: 0,
    creator: 0,
    ruler: 0,
  };

  for (const question of ARCHETYPE_QUESTIONS) {
    const letter = answers[question.id];
    if (!letter) continue;
    const opt = question.options.find((o) => o.letter === letter);
    if (opt) counts[opt.archetype] += 1;
  }

  // Индекс архетипа в каноничном порядке — для стабильного tie-break
  const orderIndex = (key: ArchetypeKey) => ARCHETYPE_ORDER.indexOf(key);

  const scores: ArchetypeScore[] = ARCHETYPE_ORDER.map((key) => ({
    key,
    name: ARCHETYPES[key].name,
    score: counts[key],
    percent: Math.round((counts[key] * 100 * 10) / TOTAL_QUESTIONS) / 10,
  })).sort((a, b) => {
    if (b.score !== a.score) return b.score - a.score;
    return orderIndex(a.key) - orderIndex(b.key);
  });

  const top = scores.slice(0, 3);
  const maxScore = scores[0]?.score ?? 0;
  const tiedCount = scores.filter((s) => s.score === maxScore && maxScore > 0).length;
  const tiedTop = tiedCount >= 2;

  return { scores, top, tiedTop, tiedCount };
}