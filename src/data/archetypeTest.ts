// Хелпер для компактного описания вопросов
function q(
  id: string,
  title: string,
  opts: [string, ArchetypeKey][],
): ArchetypeQuestion {
  const letters: OptionLetter[] = ['A', 'B', 'C', 'D'];
  return {
    id,
    title,
    options: opts.map(([text, archetype], i) => ({
      letter: letters[i],
      text,
      archetype,
    })),
  };
}

